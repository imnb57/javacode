package if_else;
// 4. Write a JAVA program to check whether a number is even or odd.
public class q4 {
    public static void main(String[] args){
        int a = 2;
        if (a%2 == 0){
            System.out.println("The number is even");
        }
        else if(a%2!=0){
            System.out.println("The number is odd");
        }
    }
}
